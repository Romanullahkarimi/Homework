package HomeWork_1;

public class VariablesMonth {
    public static void main(String[] args) {

        int month=4;

        if(month==1){
            System.out.println("january");
        }
        else if(month==2){
            System.out.println("february");
        }else if(month==3){
            System.out.println("march");
        }else if(month==4){
            System.out.println("april");
        }
        else{
            System.out.println("please enter from 12 month");
        }
    }
}
